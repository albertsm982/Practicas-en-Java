package juego.util;

/**
 * Clase dirección que indica las direcciones posibles a finalizar la partida.
 *
 * @author Alberto Santos Martínez
 */
public enum Dirección {

	/** Horizontal. */
	HORIZONTAL,
	/** Vertical. */
	VERTICAL,
	/** Diagonal de suroeste a noroeste. */
	DIAGONAL_SO_NE,
	/** Diagonal de noroeste a sureste. */
	DIAGONAL_NO_SE;
}
